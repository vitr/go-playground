func findMostCommonTitle(myId, degreesOfSeparation int) string {
	queue := []int{myId}
	seen := map[int]bool{}
	jobs := map[string]int{}

	for i := 0; i <= degreesOfSeparation; i++ {
		newQueue := []int{}
		for _, v := range queue {
			if !seen[v] {
				newQueue = append(newQueue, v)
			}
		}
		queue = newQueue
		newQueue = []int{}
		for _, v := range queue {
			user := getUser(v)
			jobs[user.title] = jobs[user.title] + 1
			seen[user.id] = true
			newQueue = append(newQueue, user.connections...)
		}
		queue = newQueue
		/*
		   return Object.keys(jobs)
		     .map((job) => [job, jobs[job]])
		     .sort((a, b) => {
		       if (a[1] > b[1]) return -1;
		       if (a[1] < b[1]) return 1;
		       return 0;
		     })[0][0]
		*/
	}
	r := ""
	max := 0
	for k, v := range jobs {
		if v > max {
			max = v
			r = k
		}
	}
	return r
}

